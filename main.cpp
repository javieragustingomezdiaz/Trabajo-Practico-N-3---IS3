#include <iostream>                                   // Programa que muestra dos valores, uno de tipo entero y otro de tipo real 

int main()                                          
{
    int n = 15;                                       // la variable n toma el valor 15
    double suma = 35.2;                               // la variable suma toma el valor 35.2
    std::cout << "n = " << n << std::endl             // se muestran en pantalla los valores de la variable n y de la variable suma
        << "suma = " << suma << std::endl;
    return 0;
}


